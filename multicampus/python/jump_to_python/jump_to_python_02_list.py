a

# del 함수 사용해 리스트 요소 삭제하기 / del 객체
a = [1, 2, 3]
del a[1]
a

# 리스트 관련 함수들
# 리스트에 요소 추가(append)
a = [1, 2, 3]
a.append(4)
a
a.append([5,6])
a

# 리스트 정렬(sort)
a = [1, 4, 3, 2] # 오름 차순으로 정렬되는 것을 볼 수 있다.
a.sort()
a
a = ['a', 'c', 'b']
a.sort()
a

# 리스트 뒤집기(reverse)
a = ['a', 'c', 'b'] # 현재의 리스트를 그대로 거구로 뒤집는다.  
a.reverse()
a

# 위치변환(index)
a = [1,2,3]
a.index(3)
a.index(1)
a.index(0) # 리스트에 존재하지 않는 값은 ValueError 발생

# 리스트에 요소 삽입(insert)
a = [1, 2, 3]
a.insert(0, 4) #해당 인덱스에 갑을 추가한다 기존 값은 +1 인덱스로 밀려난다.
a

# 리스트 요소 제거(remove)
a = [1, 2, 3, 1, 2, 3]
a.remove(3) # 요소 값으로 제거한다 값이 두 개 이면 1개만 제거 된다.
a

# 리스트 요소 끄집어내기(pop) 
a = [1,2,3]
a.pop() # 리스트 마지막 요소를 반환하고 요소는 리스트 내에서 삭제한다.
a

# 리스트에 포함된 요소 x의 개수 세기
a = [1,2,3,1]
a.count(1)

# 리스트 확장(extend)
a = [1,2,3]
a.extend([4,5])
a

b = [6, 7]
a.extend(b)
a