# 초보자가 범하기 쉬운 리스트 연산오류
a = [1,2,3]
a[2] + "hi" # a[2]의 값이 정수이기에 형 오류(TypeError)가 발생했다.
str(a[2]) + 'hi' # 형변환으로 해결

# 리스트에서 값 수정하기
a = [1,2,3]
